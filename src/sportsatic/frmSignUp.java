    private void btnRegistrarmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarmeActionPerformed
        Connection con = Conexion.getConexion();
        if (date == null) {
            date = new Date();
            formato = new SimpleDateFormat("YY/MM/dd");

        }
        try {
            Statement st = con.createStatement();
            ResultSet rs;

            String nombre = txtNombre.getText().trim();
            String apellido = txtApellido.getText().trim();
            String Genero = txtGenero.getSelectedItem().toString().trim();
            String identificacion = txtIdentificacion.getText().trim();
            Integer edad = (Integer) txtEdad.getValue();
            String correo = txtCorreo.getText().trim();
            String usuario = txtUsuario.getText().trim();
            String contrase単a = String.valueOf(txtContrase単a.getPassword()).trim();
            String fecha = formato.format(date);
            String rol = "Usuario";

            if (nombre.length() > 0 && apellido.length() > 0 && identificacion.length() > 0 && edad > 0 && correo.length() > 0 && usuario.length() > 0 && contrase単a.length() > 0) {
                rs = st.executeQuery("SELECT * FROM Persona where Usuario='" + usuario + "' or Identificacion='" + identificacion + "';");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "El nombre de usuario no se encuentra disponible", "Error", JOptionPane.ERROR_MESSAGE);
                } else {

                    int x = st.executeUpdate("INSERT INTO Persona (Nombre,Apellido,Genero,Identificacion,Edad,Correo,Usuario,Contrase単a,Fecha,Rol) VALUES"
                            + "('" + nombre + "','" + apellido + "','" + Genero + "','" + identificacion + "'," + edad + ",'" + correo + "','" + usuario + "','" + contrase単a + "','" + fecha + "','" + rol + "');");

                                         
                        
                    if (x == 1) {
                        JOptionPane.showMessageDialog(null, "Te has registrado exitosamente", "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
                        //Agregamos a la tabla usuario
                        
                        rs = st.executeQuery("SELECT * FROM Persona where Usuario='"+usuario+"';");
                        rs.next();
                        st.executeUpdate("INSERT INTO Usuario (persona_fk) VALUES ("+rs.getInt("idPersona")+");");
                        
                        this.dispose();
                        new frmLogin().setVisible(true);
                    } else {

                        JOptionPane.showMessageDialog(null, "No se pudo hacer el registro", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Rellene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnRegistrarmeActionPerformed

